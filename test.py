# Find the peak element (element greater than or equal to its neighbors).

x = [1, 5, 2, 6, 1, 9]

def find_peak(arr):
    
    for i in range(1, len(arr) - 1):
        if arr[i] >= arr[i - 1] and arr[i] >= arr[i + 1]:
            return arr[i]
    return None

